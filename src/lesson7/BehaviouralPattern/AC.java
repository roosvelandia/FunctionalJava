package lesson7.BehaviouralPattern;

public class AC {

    public void turnOn() {
        System.out.println("AC is on");
    }

    public void turnOff() {
        System.out.println("AC is off");
    }

    public void incTemp(){
        System.out.println("Increasing Temperature");
    }

    public void decTemp(){
        System.out.println("Decreasing Temperature");
    }
}
